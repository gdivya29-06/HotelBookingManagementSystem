/**
 * Use Case 1: Application Entry & Welcome Message
 * This class represents the entry point of the Hotel Booking Management System.
 * It demonstrates how a Java application starts execution and displays
 * a welcome message with application name and version.
 *
 * @author Divya
 * @version 1.0
 */
public class UseCase1HotelBookingApp {

    /**
     * Main method - entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args Command-line arguments
     */
    public static void main(String[] args) {
        System.out.println("======================================");
        System.out.println("      Welcome to Book My Stay App     ");
        System.out.println("   Hotel Booking Management System    ");
        System.out.println("           Version: 1.0               ");
        System.out.println("======================================");
    }
}
